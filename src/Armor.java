public class Armor {
    private int health;
    private int atk;
    private int dodge;
    private String type;
    private String name;

    public Armor(int health, int atk, int dodge, String type, String name){
        this.health = health;
        this.atk = atk;
        this.dodge = dodge;
        this.type = type;
        this.name = name;
    }

}
